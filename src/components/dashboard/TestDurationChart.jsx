import { Line } from 'react-chartjs-2';

export default function TestDurationChart({ durations }) {
  const data = {
    labels: durations.map((d, i) => `Test ${i + 1}`),
    datasets: [
      {
        label: 'Average Duration (minutes)',
        data: durations,
        borderColor: 'rgb(139, 92, 246)',
        backgroundColor: 'rgba(139, 92, 246, 0.1)',
        fill: true,
        tension: 0.4,
      }
    ]
  };

  const options = {
    responsive: true,
    maintainAspectRatio: false,
    plugins: {
      legend: {
        display: false,
      },
    },
    scales: {
      y: {
        beginAtZero: true,
        title: {
          display: true,
          text: 'Minutes'
        }
      }
    }
  };

  return (
    <div style={{ height: '300px' }}>
      <Line data={data} options={options} />
    </div>
  );
}